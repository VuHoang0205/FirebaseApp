package net.hailm.firebaseapp.model.dbmodels;

public class UtilityModel {
    private String uitlityId;
    private String name;
    private String image;

    public UtilityModel() {
    }

    public String getUitlityId() {
        return uitlityId;
    }

    public void setUitlityId(String uitlityId) {
        this.uitlityId = uitlityId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
